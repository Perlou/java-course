package phase10;

/**
 * Phase 10 - Lesson 1: 微服务架构概述
 * 
 * 🎯 学习目标:
 * 1. 理解架构演进历程
 * 2. 掌握微服务设计原则
 * 3. 了解 Spring Cloud 生态
 */
public class MicroserviceIntro {

    public static void main(String[] args) {
        System.out.println("=".repeat(60));
        System.out.println("Phase 10 - Lesson 1: 微服务架构概述");
        System.out.println("=".repeat(60));

        // 1. 架构演进
        System.out.println("\n【1. 架构演进历程】");
        System.out.println("""
                ┌─────────────────────────────────────────────────────────┐
                │                    架构演进                             │
                ├─────────────────────────────────────────────────────────┤
                │                                                         │
                │  单体架构 (Monolithic)                                  │
                │  ┌─────────────────────────────────┐                   │
                │  │  用户模块 │ 订单模块 │ 商品模块  │                   │
                │  │         一个应用程序            │                   │
                │  └─────────────────────────────────┘                   │
                │  优点: 开发简单，部署方便                               │
                │  缺点: 耦合度高，扩展困难，技术栈单一                   │
                │                     ↓                                  │
                │  垂直架构 (Vertical)                                   │
                │  ┌─────────┐ ┌─────────┐ ┌─────────┐                  │
                │  │ 用户系统 │ │ 订单系统 │ │ 商品系统 │                  │
                │  └─────────┘ └─────────┘ └─────────┘                  │
                │  优点: 按业务拆分，相对独立                             │
                │  缺点: 系统间重复功能，调用复杂                         │
                │                     ↓                                  │
                │  SOA 架构 (Service-Oriented)                           │
                │  ┌─────────┐ ┌─────────┐ ┌─────────┐                  │
                │  │ 服务A   │ │ 服务B   │ │ 服务C   │                  │
                │  └────┬────┘ └────┬────┘ └────┬────┘                  │
                │       └──────────┼──────────┘                         │
                │             [ESB 企业服务总线]                         │
                │  优点: 服务复用，集中治理                               │
                │  缺点: ESB 过重，部署复杂                              │
                │                     ↓                                  │
                │  微服务架构 (Microservices)                            │
                │  ┌───┐ ┌───┐ ┌───┐ ┌───┐ ┌───┐                       │
                │  │ A │ │ B │ │ C │ │ D │ │ E │                       │
                │  └───┘ └───┘ └───┘ └───┘ └───┘                       │
                │  独立部署 │ 独立扩展 │ 技术多样 │ 去中心化              │
                └─────────────────────────────────────────────────────────┘
                """);

        // 2. 微服务特点
        System.out.println("=".repeat(60));
        System.out.println("【2. 微服务核心特点】");
        System.out.println("""
                ┌────────────────────┬────────────────────────────────────┐
                │       特点          │              说明                  │
                ├────────────────────┼────────────────────────────────────┤
                │ 单一职责            │ 每个服务只做一件事                 │
                │ 独立部署            │ 服务可独立发布，不影响其他服务     │
                │ 独立扩展            │ 根据需求单独扩容某个服务           │
                │ 技术异构            │ 不同服务可用不同技术栈             │
                │ 去中心化            │ 无单点故障，分布式治理             │
                │ 容错设计            │ 服务故障不影响整体系统             │
                │ 自动化              │ CI/CD，自动化测试和部署            │
                └────────────────────┴────────────────────────────────────┘

                微服务 vs 单体:

                单体架构:
                ┌───────────────────────────────────────┐
                │              一个大应用               │
                │  修改任何功能都需要重新部署整个系统   │
                └───────────────────────────────────────┘

                微服务架构:
                ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐ ┌─────┐
                │用户 │ │订单 │ │商品 │ │库存 │ │支付 │
                └─────┘ └─────┘ └─────┘ └─────┘ └─────┘
                  各自独立部署，按需扩展
                """);

        // 3. 微服务挑战
        System.out.println("=".repeat(60));
        System.out.println("【3. 微服务带来的挑战】");
        System.out.println("""
                ⚠️ 微服务不是银弹，带来新的复杂性:

                1. 服务治理
                   - 服务如何注册和发现?
                   - 服务间如何通信?
                   - 如何实现负载均衡?

                2. 数据一致性
                   - 分布式事务如何处理?
                   - 如何保证最终一致性?

                3. 运维复杂度
                   - 如何监控众多服务?
                   - 如何追踪调用链路?
                   - 日志如何聚合?

                4. 网络问题
                   - 服务调用超时?
                   - 服务不可用如何处理?
                   - 如何限流熔断?

                5. 部署复杂度
                   - 如何管理多个服务的部署?
                   - 如何实现灰度发布?

                解决方案 → Spring Cloud 生态
                """);

        // 4. Spring Cloud 生态
        System.out.println("=".repeat(60));
        System.out.println("【4. Spring Cloud 生态】");
        System.out.println("""
                Spring Cloud 提供微服务全套解决方案:

                ┌─────────────────────────────────────────────────────────┐
                │                Spring Cloud 生态                        │
                ├─────────────────────────────────────────────────────────┤
                │                                                         │
                │  服务治理:                                              │
                │  ├── Nacos / Eureka     服务注册与发现                  │
                │  ├── OpenFeign          声明式 HTTP 调用                │
                │  └── LoadBalancer       客户端负载均衡                  │
                │                                                         │
                │  流量控制:                                              │
                │  ├── Sentinel / Hystrix 熔断降级、限流                  │
                │  └── Gateway / Zuul     API 网关                       │
                │                                                         │
                │  配置管理:                                              │
                │  └── Nacos Config / Config Server 配置中心              │
                │                                                         │
                │  分布式事务:                                            │
                │  └── Seata             分布式事务                       │
                │                                                         │
                │  可观测性:                                              │
                │  ├── Sleuth + Zipkin   链路追踪                         │
                │  └── SkyWalking        APM 监控                         │
                │                                                         │
                └─────────────────────────────────────────────────────────┘

                Spring Cloud Alibaba (推荐):
                - Nacos: 服务发现 + 配置中心
                - Sentinel: 流量控制
                - Seata: 分布式事务
                - RocketMQ: 消息队列
                """);

        // 5. 微服务架构图
        System.out.println("=".repeat(60));
        System.out.println("【5. 完整微服务架构】");
        System.out.println("""
                ┌─────────────────────────────────────────────────────────────────┐
                │                       完整微服务架构                             │
                ├─────────────────────────────────────────────────────────────────┤
                │                                                                 │
                │   客户端 (Web/App)                                              │
                │        │                                                        │
                │        ▼                                                        │
                │   ┌─────────────────┐                                          │
                │   │   API Gateway   │ ← 统一入口、路由、鉴权、限流              │
                │   └────────┬────────┘                                          │
                │            │                                                    │
                │     ┌──────┴──────┬──────────────┐                             │
                │     ▼             ▼              ▼                             │
                │ ┌────────┐  ┌────────┐    ┌────────┐                           │
                │ │用户服务 │  │商品服务 │    │订单服务 │ ← 业务微服务             │
                │ └───┬────┘  └───┬────┘    └───┬────┘                           │
                │     │           │             │                                │
                │     └───────────┼─────────────┘                                │
                │                 │                                              │
                │     ┌───────────┴───────────┐                                  │
                │     ▼                       ▼                                  │
                │ ┌────────────┐      ┌──────────────┐                           │
                │ │  Nacos     │      │  Sentinel    │                           │
                │ │ 注册中心   │      │  熔断限流    │                           │
                │ │ 配置中心   │      └──────────────┘                           │
                │ └────────────┘                                                 │
                │                                                                 │
                │ ┌─────────────────────────────────────────────────────────┐    │
                │ │                    基础设施层                            │    │
                │ │  MySQL │ Redis │ RocketMQ │ Elasticsearch │ MinIO      │    │
                │ └─────────────────────────────────────────────────────────┘    │
                │                                                                 │
                │ ┌─────────────────────────────────────────────────────────┐    │
                │ │                    可观测性                              │    │
                │ │  Prometheus │ Grafana │ SkyWalking │ ELK              │    │
                │ └─────────────────────────────────────────────────────────┘    │
                └─────────────────────────────────────────────────────────────────┘
                """);

        // 6. 微服务设计原则
        System.out.println("=".repeat(60));
        System.out.println("【6. 微服务设计原则】");
        System.out.println("""
                1. 单一职责原则
                   - 每个服务只负责一个业务领域
                   - 高内聚，低耦合
                   - 例: 用户服务只处理用户相关业务

                2. 服务自治原则
                   - 独立数据库
                   - 独立部署
                   - 独立扩展
                   - 服务间通过 API 通信

                3. 轻量级通信
                   - RESTful API (HTTP/JSON)
                   - gRPC (高性能场景)
                   - 消息队列 (异步通信)

                4. 容错设计
                   - 服务降级
                   - 熔断机制
                   - 重试策略
                   - 超时控制

                5. 自动化优先
                   - CI/CD 流水线
                   - 容器化部署
                   - 配置管理
                   - 监控告警

                6. 领域驱动设计 (DDD)
                   - 按业务边界拆分服务
                   - 识别领域模型
                   - 限界上下文
                """);

        // 7. 如何拆分微服务
        System.out.println("=".repeat(60));
        System.out.println("【7. 如何拆分微服务】");
        System.out.println("""
                拆分原则:

                1. 按业务能力拆分
                   电商系统拆分示例:
                   ├── 用户服务 (user-service)
                   ├── 商品服务 (product-service)
                   ├── 订单服务 (order-service)
                   ├── 库存服务 (inventory-service)
                   ├── 支付服务 (payment-service)
                   └── 物流服务 (logistics-service)

                2. 拆分粒度
                   ❌ 太细: 服务爆炸，运维困难
                   ❌ 太粗: 跟单体差不多
                   ✅ 适中: 2-pizza 团队原则 (一个服务由两个披萨能喂饱的团队维护)

                3. 何时不拆
                   - 项目初期，业务不明确
                   - 团队规模小
                   - 没有容器和自动化能力
                   → 先做好模块化设计，等业务稳定再拆分

                4. 渐进式拆分
                   单体 → 识别边界 → 解耦模块 → 拆分服务 → 独立部署
                """);

        System.out.println("\n" + "=".repeat(60));
        System.out.println("💡 微服务是架构风格，不是银弹");
        System.out.println("💡 Spring Cloud Alibaba 是国内主流方案");
        System.out.println("💡 先做好模块化，再考虑微服务拆分");
        System.out.println("=".repeat(60));
    }
}

