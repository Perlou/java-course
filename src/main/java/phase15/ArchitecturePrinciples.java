    private static void capTheorem() {
        System.out.println("\n【1. CAP 定理】");
        System.out.println("-".repeat(50));

        String cap = """

                ═══════════════════════════════════════════════════════════
                                  CAP 定理
                ═══════════════════════════════════════════════════════════

                分布式系统最多只能同时满足以下三项中的两项：

                C - Consistency (一致性)
                ─────────────────────────────────────────────────────────
                • 所有节点在同一时间看到相同的数据
                • 读操作总是返回最新写入的数据

                A - Availability (可用性)
                ─────────────────────────────────────────────────────────
                • 每个请求都能收到（非错误）响应
                • 不保证返回最新数据

                P - Partition Tolerance (分区容错)
                ─────────────────────────────────────────────────────────
                • 网络分区发生时，系统仍能继续运行
                • 分布式系统必须支持 P


                ═══════════════════════════════════════════════════════════
                                  实际选择：CP vs AP
                ═══════════════════════════════════════════════════════════

                由于 P 是必须的，实际选择是 CP 或 AP：

                CP 系统（选择一致性）
                ─────────────────────────────────────────────────────────
                • 网络分区时，可能拒绝服务
                • 保证数据一致性
                • 适用场景：金融交易、库存扣减、配置管理
                • 代表系统：ZooKeeper, etcd, HBase, Redis Cluster

                AP 系统（选择可用性）
                ─────────────────────────────────────────────────────────
                • 网络分区时，继续提供服务
                • 可能返回旧数据
                • 适用场景：社交动态、商品详情、购物车
                • 代表系统：Cassandra, DynamoDB, CouchDB


                ═══════════════════════════════════════════════════════════
                                  场景分析
                ═══════════════════════════════════════════════════════════

                场景1: 银行转账
                • 必须选择 CP
                • 宁可服务不可用，也不能出现数据不一致
                • A 转给 B 100元，必须保证 A-100, B+100 同时生效

                场景2: 微博点赞数
                • 可以选择 AP
                • 允许短暂不一致（某些用户看到 999，某些看到 1000）
                • 保证高可用，最终一致即可

                场景3: 电商库存
                • 核心库存扣减选择 CP
                • 库存展示可以选择 AP（允许超卖提示）
                """;
        System.out.println(cap);
    }

    private static void baseTheory() {
        System.out.println("\n【2. BASE 理论】");
        System.out.println("-".repeat(50));

        String base = """

                ═══════════════════════════════════════════════════════════
                                  BASE 理论
                ═══════════════════════════════════════════════════════════

                BASE 是对 CAP 中 AP 的延伸，是 ACID 的替代方案

                BA - Basically Available (基本可用)
                ─────────────────────────────────────────────────────────
                • 系统出现故障时，允许损失部分可用性
                • 例如：响应时间略有增加（正常 100ms → 降级 2s）
                • 例如：功能降级（秒杀时引导页代替商品详情）

                S - Soft State (软状态)
                ─────────────────────────────────────────────────────────
                • 允许数据存在中间状态
                • 不同节点的数据副本在同一时刻可以不一致
                • 例如：订单状态 "支付中" 是中间状态

                E - Eventually Consistent (最终一致性)
                ─────────────────────────────────────────────────────────
                • 经过一段时间后，所有数据达成一致
                • 不保证实时一致，但保证最终一致
                • 一致性窗口：从写入到所有副本一致的时间


                ═══════════════════════════════════════════════════════════
                                  ACID vs BASE
                ═══════════════════════════════════════════════════════════

                ┌──────────────────┬────────────────────────────────────┐
                │      ACID         │              BASE                  │
                ├──────────────────┼────────────────────────────────────┤
                │ 强一致性          │ 最终一致性                         │
                │ 隔离性            │ 软状态                             │
                │ 悲观锁            │ 乐观锁                             │
                │ 单机事务          │ 分布式事务                         │
                │ 传统数据库        │ NoSQL、分布式系统                  │
                └──────────────────┴────────────────────────────────────┘


                ═══════════════════════════════════════════════════════════
                                  一致性级别
                ═══════════════════════════════════════════════════════════

                强一致性 (Strong Consistency)
                • 写入后立即可读到
                • 性能最低

                弱一致性 (Weak Consistency)
                • 不保证何时能读到
                • 性能最高

                最终一致性 (Eventual Consistency)
                • 在一致性窗口后可读到
                • 性能与一致性的平衡

                读己之写 (Read Your Writes)
                • 自己写的数据立即可读
                • 适合用户场景
                """;
        System.out.println(base);
    }

    private static void twelveFactorApp() {
        System.out.println("\n【3. 12-Factor App】");
        System.out.println("-".repeat(50));

        String twelveFactor = """

                ═══════════════════════════════════════════════════════════
                                  12-Factor App
                ═══════════════════════════════════════════════════════════

                云原生应用设计的 12 条最佳实践原则：


                1. Codebase (代码库)
                ─────────────────────────────────────────────────────────
                • 一份代码，多次部署
                • 使用版本控制（Git）
                • 不同环境通过配置区分，而非代码分支

                2. Dependencies (依赖)
                ─────────────────────────────────────────────────────────
                • 显式声明依赖（pom.xml, package.json）
                • 不依赖系统级工具
                • 使用依赖隔离（不与其他应用共享）

                3. Config (配置)
                ─────────────────────────────────────────────────────────
                • 配置存储在环境变量中
                • 配置与代码分离
                • 不同环境使用不同配置

                4. Backing Services (后端服务)
                ─────────────────────────────────────────────────────────
                • 把后端服务当作附加资源
                • 数据库、消息队列、缓存都应可替换
                • 本地 MySQL 和云上 RDS 应可无缝切换

                5. Build, Release, Run (构建、发布、运行)
                ─────────────────────────────────────────────────────────
                • 严格分离构建和运行阶段
                • 构建：代码 → 可执行包
                • 发布：构建产物 + 配置
                • 运行：在执行环境中启动

                6. Processes (进程)
                ─────────────────────────────────────────────────────────
                • 应用以无状态进程运行
                • 状态存储在后端服务（Redis, DB）
                • 不依赖本地文件系统

                7. Port Binding (端口绑定)
                ─────────────────────────────────────────────────────────
                • 通过端口绑定提供服务
                • 不依赖外部 Web 服务器
                • 内嵌 Web 服务器（Spring Boot 内嵌 Tomcat）

                8. Concurrency (并发)
                ─────────────────────────────────────────────────────────
                • 通过进程模型进行扩展
                • 水平扩展而非垂直扩展
                • 多个进程处理不同类型工作

                9. Disposability (易处理)
                ─────────────────────────────────────────────────────────
                • 快速启动和优雅关闭
                • 能够随时被终止
                • 支持快速弹性扩缩容

                10. Dev/Prod Parity (开发/生产等价)
                ─────────────────────────────────────────────────────────
                • 保持开发、预发布、生产环境一致
                • 使用相同的后端服务
                • 缩短从开发到部署的时间

                11. Logs (日志)
                ─────────────────────────────────────────────────────────
                • 把日志当作事件流
                • 不管理日志文件
                • 输出到 stdout，由平台收集

                12. Admin Processes (管理进程)
                ─────────────────────────────────────────────────────────
                • 管理任务作为一次性进程运行
                • 数据迁移、临时脚本
                • 与应用代码一起发布
                """;
        System.out.println(twelveFactor);
    }

    private static void architectureTradeoffs() {
        System.out.println("\n【4. 架构权衡】");
        System.out.println("-".repeat(50));

        String tradeoffs = """

                ═══════════════════════════════════════════════════════════
                                  架构设计核心思想
                ═══════════════════════════════════════════════════════════

                "架构是关于重要决策的艺术，这些决策在项目后期难以改变"
                                                      -- Robert C. Martin

                "没有银弹"
                    -- Fred Brooks

                "在架构设计中，一切都是权衡"


                ═══════════════════════════════════════════════════════════
                                  常见权衡
                ═══════════════════════════════════════════════════════════

                1. 一致性 vs 可用性
                ─────────────────────────────────────────────────────────
                • CAP 定理的核心选择
                • 根据业务场景决定优先级

                2. 性能 vs 可维护性
                ─────────────────────────────────────────────────────────
                • 过早优化是万恶之源
                • 先保证正确性和可维护性

                3. 单体 vs 微服务
                ─────────────────────────────────────────────────────────
                • 单体：简单、易部署、难扩展
                • 微服务：复杂、可扩展、运维成本高
                • 建议：从单体开始，按需拆分

                4. 同步 vs 异步
                ─────────────────────────────────────────────────────────
                • 同步：简单、直接、阻塞
                • 异步：复杂、解耦、高吞吐

                5. 读优化 vs 写优化
                ─────────────────────────────────────────────────────────
                • 读多写少：缓存、读写分离
                • 写多读少：批量写入、异步处理


                ═══════════════════════════════════════════════════════════
                                  架构决策准则
                ═══════════════════════════════════════════════════════════

                □ 最小化技术债务
                □ 延迟决策（在最后责任时刻决策）
                □ 保持架构可演进
                □ 关注核心域
                □ 简单优于复杂
                □ 可测试性
                □ 文档化重要决策（ADR）
                """;
        System.out.println(tradeoffs);

        System.out.println("\n" + "=".repeat(60));
        System.out.println("✅ 架构设计原则课程完成！");
        System.out.println("下一课: LayeredArchitecture.java");
        System.out.println("=".repeat(60));
    }
